package swu.xl.typeevaluator;

public class Point {
    //记录坐标的两个变量
    private float x;
    private float y;

    //构造方法赋值
    public Point(float x, float y) {
        this.x = x;
        this.y = y;
    }

    //get方法
    public float getX() {
        return x;
    }

    public float getY() {
        return y;
    }
}
